package level;

/**
 * Enumerations for trigger types:
 * SHIP: only ships can fire this trigger
 * SHOT: only projectiles can fire this trigger
 * DOODAD: only doodads can fire this trigger
 * TRIGGER: only other triggers can fire this trigger
 * ALL: everything can fire this trigger
 * @author proohr
 * @version 1.0
 */
public enum TriggerTypes {
	
	SHIP, SHOT, DOODAD,TRIGGER,ALL
}
